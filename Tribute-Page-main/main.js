document.getElementById("title-2").addEventListener("click", function () {
    let divToBeTargetted = document.getElementById("tribute-info")
    if (divToBeTargetted.style.display === "none") {
        divToBeTargetted.style.display = "block";
    } else {
        divToBeTargetted.style.display = "none";
    }
});


